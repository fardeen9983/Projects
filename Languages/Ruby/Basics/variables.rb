# Variables are assigned values.
# Types are inferred
name = 'John' 
age = 10

# Using + (concatenation) operation we can combine string together
# However we have to convert non strings to strings fisrt for this to work
# to_s method converts all variables to their string equivalent
puts "Hi my name is " + name + ", and my age is " +  age.to_s

